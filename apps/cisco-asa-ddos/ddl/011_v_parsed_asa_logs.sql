        grok(asa_message,
             'Teardown ICMP connection for faddr %{IP:faddr}/%{INT:faddr_id} gaddr %{IP:gaddr}/%{INT:gaddr_id} laddr %{IP:laddr}/%{INT:laddr_id}( duration %{DATA:duration})?( bytes %{INT:bytes})?'),
